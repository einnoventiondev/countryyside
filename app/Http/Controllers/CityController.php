    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $city =Continent::find($id);
        return view('modules.add-cities.edit',compact('city'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $city =Continent::find($request->id);
        $city->update([
            'city' => $request->city,
            'continent' => $request->continent,
        ]);
        return redirect()->route('city.index')->with('message', 'تم تحديث المدينة بنجاح ');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        Continent::find($id)->delete();
        return redirect()->route('city.index')->with('message', 'تم حذف المدينة بنجاح');
    }
